const models = require("../models");

const browse = (req, res) => {
  models.quantity
    .findAll()
    .then(([rows]) => {
      res.send(rows);
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

const read = (req, res) => {
  models.quantity
    .find(req.params.id)
    .then(([rows]) => {
      if (rows[0] == null) {
        res.sendStatus(404);
      } else {
        res.send(rows[0]);
      }
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

const readByValueAndTypeId = (req, res) => {
  const quantityInfo = { value: req.params.value, type_id: req.params.typeId };

  models.quantity
    .findByValueAndTypeId(quantityInfo)
    .then(([rows]) => {
      if (rows[0] == null) {
        res.send("No quantity with these values.");
      } else {
        res.send(rows[0]);
      }
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

const edit = (req, res) => {
  const quantity = req.body;

  // TODO validations (length, format...)

  quantity.id = parseInt(req.params.id, 10);

  models.quantity
    .update(quantity)
    .then(([result]) => {
      if (result.affectedRows === 0) {
        res.sendStatus(404);
      } else {
        res.sendStatus(204);
      }
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

const add = (req, res) => {
  const quantity = req.body;

  // TODO validations (length, format...)

  models.quantity
    .insert(quantity)
    .then(([result]) => {
      res.location(`/items/${result.insertId}`).sendStatus(201);
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

const destroy = (req, res) => {
  models.quantity
    .delete(req.params.id)
    .then(([result]) => {
      if (result.affectedRows === 0) {
        res.sendStatus(404);
      } else {
        res.sendStatus(204);
      }
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

module.exports = {
  browse,
  read,
  readByValueAndTypeId,
  edit,
  add,
  destroy,
};
